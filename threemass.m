function dydt = threemass(t,y,m1,m2, m3)
% [x1, y1, x2, y2, vx1, vy1, vx2, vy2]
x1 = y(1);
y1 = y(2);
x2 = y(3);
y2 = y(4);
x3 = y(5);
y3 = y(6);
vx1 = y(7);
vy1 = y(8);
vx2 = y(9);
vy2 = y(10);
vx3 = y(11);
vy3 = y(12);

r12 = ((x1-x2)^2 + (y2-y1)^2)^(3/2);
r13 = ((x1-x3)^2 + (y3-y1)^2)^(3/2);
r23 = ((x3-x2)^2 + (y2-y3)^2)^(3/2);
dydt = [vx1;
        vy1;
        vx2;
        vy2;
        vx3;
        vy3;
        (m2*(x2-x1)/r12) + (m3*(x3-x1)/r13) ;
        (m2*(y2-y1)/r12) + (m3*(y3-y1)/r13);
        (m1*(x1-x2)/r12) + (m3*(x3-x2)/r23);
        (m1*(y1-y2)/r12) + (m3*(y3-y2)/r23);
        (m1*(x1-x3)/r13) + (m3*(x2-x3)/r23);
        (m1*(y1-y3)/r13) + (m3*(y2-y3)/r23)];
end
